def _get_threshold_rule_or_404(asset, rule_code: str):
    """
    Fetch a ``ThresholdRule`` that is **reachable from this asset**:

      * sensor-scope rules whose ``sensor.device.asset == asset``;
      * device-scope rules whose ``device.asset == asset``;
      * asset-scope rules whose ``asset == asset``;
      * site-scope rules whose ``site == asset.site``.

    Global rules and rules belonging to other assets/sites are intentionally
    out of scope for the dashboard edit page; they remain editable via
    Django admin.
    """
    from django.db.models import Q
    from apps.analytics.models import ThresholdRuleScope

    qs = ThresholdRule.objects.select_related(
        "metric", "sensor", "sensor__device",
        "device", "asset", "site",
    ).filter(
        Q(
            scope_level=ThresholdRuleScope.SENSOR,
            sensor__device__asset=asset,
        )
        | Q(
            scope_level=ThresholdRuleScope.DEVICE,
            device__asset=asset,
        )
        | Q(scope_level=ThresholdRuleScope.ASSET, asset=asset)
        | Q(scope_level=ThresholdRuleScope.SITE, site=asset.site),
    )
    rule = qs.filter(code=rule_code).first()
    if rule is None:
        raise Http404(
            f"ThresholdRule '{rule_code}' is not reachable from asset "
            f"'{asset.code}'.",
        )
    return rule


class ThresholdRuleEditView(LoginRequiredMixin, View):
    """
    Operator-facing edit form for one :class:`ThresholdRule`, anchored to
    an asset so we only expose rules already reachable from that asset.

    Promotion to global scope is intentionally not exposed here — that
    would silently change semantics on a wide set of assets/sensors and
    belongs to Django admin.
    """

    template_name = "dashboard/threshold_rule_edit.html"

    def get(self, request, asset_code: str, rule_code: str):
        asset = _get_asset_or_404(asset_code)
        rule = _get_threshold_rule_or_404(asset, rule_code)
        form = ThresholdRuleEditForm(
            asset=asset, instance=rule,
            initial=ThresholdRuleEditForm.initial_from(rule),
        )
        return render(request, self.template_name, {
            "asset": asset, "rule": rule, "form": form,
            "scope_choices": THRESHOLD_SCOPE_CHOICES,
        })

    def post(self, request, asset_code: str, rule_code: str):
        asset = _get_asset_or_404(asset_code)
        rule = _get_threshold_rule_or_404(asset, rule_code)
        form = ThresholdRuleEditForm(
            request.POST, asset=asset, instance=rule,
        )
        if not form.is_valid():
            return render(request, self.template_name, {
                "asset": asset, "rule": rule, "form": form,
                "scope_choices": THRESHOLD_SCOPE_CHOICES,
                "has_errors": True,
            }, status=400)
        try:
            self._commit(form, rule)
        except Exception as exc:  # noqa: BLE001
            # Surface model-level ValidationError messages back into the
            # form so operators see *why* a save was rejected (e.g.,
            # sensor-scope rule with missing SensorMetric).
            form.add_error(None, str(exc))
            return render(request, self.template_name, {
                "asset": asset, "rule": rule, "form": form,
                "scope_choices": THRESHOLD_SCOPE_CHOICES,
                "has_errors": True,
            }, status=400)
        messages.success(
            request,
            f"Sliekšņa noteikums '{rule.code}' atjaunināts.",
        )
        return redirect(
            reverse("dashboard:asset-configure",
                    kwargs={"asset_code": asset.code})
        )

    @transaction.atomic
    def _commit(self, form, rule):
        form.apply_to(rule)
        rule.save()


