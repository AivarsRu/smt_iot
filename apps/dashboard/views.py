class EventDetailView(LoginRequiredMixin, TemplateView):
    """Server-rendered shell for ``GET /dashboard/events/<event_id>/``.

    The view itself only validates that the event exists (so unknown
    UUIDs return 404) and emits a small config block; the rest of the
    page — context, payload JSON, measurement summary, sensor/metric
    timeline — is loaded client-side from ``/api/events/<id>/`` and
    ``/api/measurements/``.
    """

    template_name = "dashboard/event_detail.html"
    http_method_names = ["get", "head", "options"]

    def get_context_data(self, **kwargs):
        context = super().get_context_data(**kwargs)
        event_id = kwargs["event_id"]
        # 404 surface so test_unknown_event_id_returns_404 holds. We
        # don't need any of the actual fields here because the page is
        # rendered client-side; but verifying existence keeps the URL
        # honest and avoids a flashing-then-broken page.
        event = get_object_or_404(
            Event.objects.only("id"), id=event_id,
        )
        context["event_id"] = str(event.id)
        # URL templates for the JS side. Both use UUID-shaped placeholders
        # so the JS does not have to special-case path segments.
        event_detail_url = reverse("event-detail", kwargs={"pk": str(event.id)})
        measurements_list_url = reverse("measurement-list")
        context["event_detail_config"] = {
            "eventId": str(event.id),
            "eventDetailUrl": event_detail_url,
            "measurementsUrl": measurements_list_url,
            "eventsListUrl": reverse("dashboard:events-list"),
            "assetDetailUrlTemplate": reverse(
                "dashboard:asset-detail",
                kwargs={"asset_identifier": "__CODE__"},
            ),
            "periods": list(EVENT_TIMELINE_PERIODS),
            "defaultPeriod": "24h",
            "timelineLimit": 1000,
        }
        return context
